package model;

public class ConnectionInfo {

	
	int departureFlightId;
	int arrivalFlightId;
	public int getDepartureFlightId() {
		return departureFlightId;
	}
	public void setDepartureFlightId(int departureFlightId) {
		this.departureFlightId = departureFlightId;
	}
	public int getArrivalFlightId() {
		return arrivalFlightId;
	}
	public void setArrivalFlightId(int arrivalFlightId) {
		this.arrivalFlightId = arrivalFlightId;
	}
	
	
	
}
